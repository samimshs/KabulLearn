export type Dictionary = {
  [Key in keyof typeof dictionaries.en]: string;
};

export const COURSE_LEVELS = {
  beginner:     { en: "Beginner",     ps: "لومړنی",  fa: "ابتدایی" },
  intermediate: { en: "Intermediate", ps: "منځنی",   fa: "متوسط"   },
  advanced:     { en: "Advanced",     ps: "پرمختللی", fa: "پیشرفته" },
} as const;

export type CourseLevel = keyof typeof COURSE_LEVELS;

export const COURSE_LEVEL_OPTIONS: CourseLevel[] = ["beginner", "intermediate", "advanced"];

export function localizeLevel(level: string | null | undefined, locale: Locale): string {
  if (!level) return "";
  const entry = COURSE_LEVELS[level as CourseLevel];
  return entry ? entry[locale] : level;
}

export function isRtlLocale(locale: Locale) {
  return locale === "ps" || locale === "fa";
}

export function usesPashtoContent(locale: Locale) {
  return locale !== "en";
}

export function localize(
  locale: Locale,
  english: string | null | undefined,
  pashto: string | null | undefined,
  dari?: string | null | undefined
) {
  if (locale === "fa") return dari ?? english ?? "";
  if (locale === "ps") return pashto ?? english ?? "";
  return english ?? pashto ?? "";
}

export function getDirection(locale: Locale): "ltr" | "rtl" {
  return isRtlLocale(locale) ? "rtl" : "ltr";
}
